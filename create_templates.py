# ---- Утилиты построения документа -------------------------------------------

def _setup_styles(doc: Document) -> None:
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(11)
    for section in doc.sections:
        section.top_margin = Cm(1.5)
        section.bottom_margin = Cm(1.5)
        section.left_margin = Cm(2.0)
        section.right_margin = Cm(1.5)


def _add_para(
    doc: Document,
    text: str,
    *,
    bold: bool = False,
    align=WD_ALIGN_PARAGRAPH.JUSTIFY,
    first_line: float | None = None,
    size: int | None = None,
) -> None:
    p = doc.add_paragraph()
    p.alignment = align
    if first_line is not None:
        p.paragraph_format.first_line_indent = Cm(first_line)
    run = p.add_run(text)
    run.bold = bold
    if size:
        run.font.size = Pt(size)


def _add_header_block(doc: Document) -> None:
    for line in COMPANY_HEADER_LINES:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run(line)
        run.font.size = Pt(9)
        if line == COMPANY_HEADER_LINES[0]:
            run.bold = True
    doc.add_paragraph()


def _add_recipient_block(doc: Document) -> None:
    for line in RECIPIENT_BLOCK:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        run = p.add_run(line)
        run.font.size = Pt(11)
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    p.add_run("Исх. б/н от {{ doc_date }} г.")
    doc.add_paragraph()


def _add_title(doc: Document, title: str, subtitle: str | None = None) -> None:
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(title)
    run.bold = True
    run.font.size = Pt(14)
    if subtitle:
        p2 = doc.add_paragraph()
        p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r2 = p2.add_run(subtitle)
        r2.bold = True
        r2.font.size = Pt(11)
    doc.add_paragraph()


def _add_legal_block(doc: Document) -> None:
    _add_para(doc, LEGAL_GROUNDS, first_line=1.25)
    for bullet in RIGHTS_BULLETS:
        _add_para(doc, bullet, first_line=1.25)
    _add_para(doc, CLAUSE_5_1, first_line=1.25)
    _add_para(doc, CLAUSE_1031, first_line=1.25)
    _add_para(doc, CLAUSE_9_9, first_line=1.25)


def _add_violations_block(doc: Document) -> None:
    """Список нарушений рендерится docxtpl-циклом по списку violations."""
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(1.25)
    p.add_run(
        "Пользователь нарушает следующие условия Договора коммерческой концессии:"
    )
    # docxtpl Jinja-цикл — каждый элемент на отдельной строке с номером.
    p_loop = doc.add_paragraph()
    p_loop.add_run("{% for v in violations %}{{ loop.index }}) {{ v }}\n{% endfor %}")


def _add_tail_block(doc: Document, demand_text: str) -> None:
    _add_para(doc, CLAUSE_309_310, first_line=1.25)
    _add_para(doc, CLAUSE_12_4, first_line=1.25)
    _add_para(doc, CLAUSE_12_5, first_line=1.25)
    _add_para(doc, CONCLUSION, first_line=1.25)

    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.add_run("ТРЕБУЕМ:").bold = True
    _add_para(doc, demand_text, first_line=1.25)
    _add_para(doc, FINAL_WARNING, first_line=1.25)


def _add_signature(doc: Document) -> None:
    doc.add_paragraph()
    p = doc.add_paragraph()
    p.add_run("Генеральный директор\nООО «Хлебник Франчайзинг»\t\t\t\t\t\tМ.А. Манаков")


